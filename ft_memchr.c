#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t i;

	i = 0;
	while ((((unsigned char *)s)[i] != '\0') && ( i < n))
	{
		if (((unsigned char *)s)[i] == ((unsigned char)c))
		{
			return ((char *)&s[i]);
		}
		i++;
	}
	if (c == '\0')
	{
		return ((char *)&s[i]);
	}
	return (NULL);
}
