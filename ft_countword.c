#include "libft.h"

int		ft_countword(char const *s, char c)
{
	int		i;
	int		ctr;

	i = 0;
	ctr = 0;
	while (s[i] != '\0')
	{
		if (s[i] != c)
			ctr++;
		i++;
	}
	return (ctr);
}
